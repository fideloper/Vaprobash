    echo "  ${0} enable <package>"
    echo "      enable a package"
    echo
    echo "  ${0} disable <package>"
    echo "      disable a package"
    echo
    echo "  ${0} configure <package>"
    echo "      configure a package"
    echo
    echo "  ${0} provision"
    echo "      execute the provisioner"
    echo
}
